    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int filas;
        int columnas = 3;
        filas = Integer.parseInt(jTextField1.getText());

        DefaultTableModel modelo = new DefaultTableModel(filas, columnas);

        String datos[] = new String[columnas];
        datos[0] = "Numero De ID";
        datos[1] = "Peso(KG)";
        datos[2] = "Altura (Meteos)";

        modelo.setColumnIdentifiers(datos);

        jTable1.setModel(modelo);

        matriz = new double[filas][columnas];

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        name = JOptionPane.showInputDialog("Cual es su nombre");
        JOptionPane.showInputDialog("Tipo De Identificaci√≥n");

        //llenado de matriz
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            for (int j = 0; j < jTable1.getColumnCount(); j++) {
                matriz[i][j] = Double.parseDouble(jTable1.getValueAt(i, j).toString());
            }
        }
        JOptionPane.showMessageDialog(null, "Guardado Exitoso");

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < matriz.length; i++) {
            peso = matriz[i][1];
            altura = matriz [i][2];
        }

        calculoIMC = peso / Math.pow(altura, 2);
        if (calculoIMC <= 18.8) {
            JOptionPane.showMessageDialog(null, "El IMC De " + name + " Es De " + calculoIMC + " PESO INFERIOR AL NNORMAL");
        } else if (calculoIMC >= 18.8 && calculoIMC <= 24.9) {
            JOptionPane.showMessageDialog(null, "El IMC De " + name + " Es De " + calculoIMC + " PESO NORMAL");
        } else if (calculoIMC >= 25.0 && calculoIMC <= 29.9) {
            JOptionPane.showMessageDialog(null, "El IMC De " + name + " Es De " + calculoIMC + " PESP SUPERIOR AL NORMAL");
        } else {
            JOptionPane.showMessageDialog(null, "El IMC De " + name + " Es De " + calculoIMC + " OBESIDA");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

